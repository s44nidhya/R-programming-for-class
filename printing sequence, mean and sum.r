# Create sequence from 20 to 50
seq_20_50 <- seq(20, 50)
cat("Sequence from 20 to 50:\n")
print(seq_20_50)

# Calculate mean of numbers from 20 to 60
seq_20_60 <- seq(20, 60)
mean_20_60 <- mean(seq_20_60)
cat("\nMean of numbers from 20 to 60:", mean_20_60)

# Calculate sum of numbers from 51 to 91
seq_51_91 <- seq(51, 91)
sum_51_91 <- sum(seq_51_91)
cat("\n  Sum of numbers from 51 to 91:", sum_51_91)